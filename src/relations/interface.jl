# relations/interface.jl ‚Äî the uniform three-verb contract for relations,
# the @relation declaration macro, and the adoption surface.
#
# A relation is an *identity among observables or exponents* ‚Äî a generic,
# model-independent statement.  Each is DECLARED EXACTLY ONCE via
# [`@relation`](@ref); everything else (solving for any affine variable,
# checking, Œ≤-or-T normalization, registry membership, introspection) is
# derived mechanically.  No hand-written rearrangements, no per-relation
# boilerplate.
#
# Architecture:
#
#   public verbs   residual / check / solve      (Œ≤-or-T normalization, one place)
#   kernels        _residual (ONE per relation)  |  _solve (rare, non-affine only)
#   traits         variables(rel), domain(rel)
#   registry       all_relations / applicable_relations / relation_report / check_all

"""
    AbstractRelation

Abstract parent type for physics relations.  Concrete relations are
declared with [`@relation`](@ref) and implement the three verbs:

- [`residual`](@ref)`(rel; vars...)` ‚Äî signed violation; `0` ‚áî satisfied.
- [`check`](@ref)`(rel; atol=0, vars...)` ‚Äî `|residual| ‚â§ atol`.
- [`solve`](@ref)`(rel, Val(:x); vars...)` ‚Äî the value of `x` implied by
  the remaining variables.

**Exact-arithmetic contract**: `residual` and `solve` must not promote
their inputs ‚Äî `Rational` in ‚áí `Rational` out, so exactly-known values
(e.g. the 2D Ising exponents) satisfy their relations *exactly*
(`residual == 0//1`), not merely to floating-point tolerance.

Relations whose variables include an inverse temperature accept either
`Œ≤` or `T` (exactly one) at every public verb; the normalization happens
once, in the verb layer ‚Äî kernels only ever see `Œ≤`.
"""
abstract type AbstractRelation end
export AbstractRelation

# the residual kernel generic function ‚Äî one method per relation,
# generated by @relation.  Public code calls `residual`, never this.
function _residual end

# ‚îÄ‚îÄ‚îÄ Traits ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

"""
    variables(rel::AbstractRelation) -> Tuple{Vararg{Symbol}}

The *required* variables of the relation (optional variables with
defaults, e.g. a site count `N = 1`, are not listed).  Filled in by
[`@relation`](@ref); used for applicability matching in
[`applicable_relations`](@ref) and by the Œ≤-or-T normalizer.
"""
function variables end
export variables

"""
    domain(rel::AbstractRelation) -> Symbol

The relation's family tag (`:scaling`, `:thermodynamic`,
`:fundamental`, `:topology`, ‚Ä¶) ‚Äî for filtering in the registry API,
and for disambiguating same-named variables across families (the
exponent `Œ≤` vs the inverse temperature `Œ≤`).
"""
function domain end
export domain

# ‚îÄ‚îÄ‚îÄ Registry ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

const _RELATION_REGISTRY = AbstractRelation[]

"""
    all_relations(; domain=nothing) -> Vector{AbstractRelation}

Every relation declared with [`@relation`](@ref) in this package,
optionally filtered by [`domain`](@ref).  The registry is what makes the
package *adoptable in one call*: a consumer never hand-lists relations
(see [`relation_report`](@ref)).
"""
function all_relations(; domain::Union{Nothing,Symbol}=nothing)
    domain === nothing && return copy(_RELATION_REGISTRY)
    return filter(r -> AbstractQAtlas.domain(r) === domain, _RELATION_REGISTRY)
end
export all_relations

# ‚îÄ‚îÄ‚îÄ Œ≤-or-T normalization (single point) ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

# `solving` is the solve-target variable (or `nothing` for residual/
# check): when Œ≤ itself is being solved for, the Œ≤-or-T demand is waived
# ‚Äî the probes supply it.  (Physics overloads Œ≤: the same normalization
# serves the inverse temperature and steps aside for the exponent.)
function _normalize_kwargs(
    rel::AbstractRelation, kwargs::NamedTuple; solving::Union{Nothing,Symbol}=nothing
)
    :Œ≤ in variables(rel) || return kwargs
    hasŒ≤ = haskey(kwargs, :Œ≤)
    hasT = haskey(kwargs, :T)
    if solving === :Œ≤
        (hasŒ≤ || hasT) && error("cannot pass Œ≤ or T while solving for :Œ≤")
        return kwargs
    end
    hasŒ≤ && hasT && error("pass either Œ≤ or T, not both")
    hasŒ≤ && return kwargs
    hasT || error("pass either Œ≤ or T")
    rest = Base.structdiff(kwargs, NamedTuple{(:T,)})
    return merge(rest, (Œ≤=1 / kwargs.T,))
end

# ‚îÄ‚îÄ‚îÄ Public verbs ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

"""
    residual(rel::AbstractRelation; vars...) -> Number

Signed violation of the relation at the given variable values; zero if
and only if the relation is satisfied.  Preserves the input number types
(see the exact-arithmetic contract on [`AbstractRelation`](@ref)).
"""
function residual(rel::AbstractRelation; kwargs...)
    return _residual(rel; _normalize_kwargs(rel, values(kwargs))...)
end
export residual

"""
    check(rel::AbstractRelation; atol=0, vars...) -> Bool

`abs(residual(rel; vars...)) ‚â§ atol`.  With the default `atol = 0` this
is an *exact* test ‚Äî appropriate for `Rational` inputs; pass an explicit
`atol` for floating-point / error-bar data.
"""
check(rel::AbstractRelation; atol=0, kwargs...) = abs(residual(rel; kwargs...)) <= atol
export check

"""
    solve(rel::AbstractRelation, ::Val{x}; vars...) -> Number

The value of variable `x` implied by the relation and the remaining
variables, e.g. `solve(Widom(), Val(:Œ≥); Œ≤=1//8, Œ¥=15//1) == 7//4`.

No per-variable rearrangements are hand-written: for any variable the
relation is *affine* in (true of almost every identity here), the answer
follows exactly from three kernel evaluations ‚Äî see the generic
`_solve`.  A relation that is non-affine in some variable provides a
specialized `_solve` for it (e.g. `FreeEnergyFromZ` for `Z`); attempting
a generic solve for a non-affine variable throws instead of silently
returning a wrong value.  Preserves input number types.
"""
function solve(rel::AbstractRelation, v::Val{X}; kwargs...) where {X}
    return _solve(rel, v; _normalize_kwargs(rel, values(kwargs); solving=X)...)
end
export solve

# Generic solve: for a residual r(x) affine in x, r(x) = r‚ÇÄ + (r‚ÇÅ ‚àí r‚ÇÄ)x
# exactly, so the root is x* = ‚àír‚ÇÄ/(r‚ÇÅ ‚àí r‚ÇÄ).  Three probes (x = 0, 1, 2)
# both solve and VERIFY affinity: the parabola test r‚ÇÇ ‚àí 2r‚ÇÅ + r‚ÇÄ must
# vanish ‚Äî exactly for exact inputs, to a relative 1e-8 for floats.
# Integer probes promote correctly, so Rational data stays Rational.
function _solve(rel::AbstractRelation, ::Val{X}; kwargs...) where {X}
    kw = values(kwargs)
    at(v) = _residual(rel; merge(kw, NamedTuple{(X,)}((v,)))...)
    r0, r1, r2 = at(0), at(1), at(2)
    curv = r2 - 2r1 + r0
    isaffine = if curv isa AbstractFloat
        scale = max(abs(r0), abs(r1), abs(r2))
        abs(curv) <= 1e-8 * max(scale, one(scale))
    else
        iszero(curv)
    end
    isaffine || error(
        "solve: $(typeof(rel)) is not affine in :$X ‚Äî define a specialized " *
        "_solve(::$(typeof(rel)), ::Val{:$X}; ...) for this variable",
    )
    b = r1 - r0
    iszero(b) && error("solve: $(typeof(rel)) does not depend on :$X")
    return -r0 / b
end

# ‚îÄ‚îÄ‚îÄ The declaration macro ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

"""
    @relation :domain Name(x, y, z, opt=default) = expr

Declare a relation ONCE.  Expands to the complete implementation:

- `struct Name <: AbstractRelation end` (docstring-attachable),
- the residual kernel `_residual(::Name; x, y, z, opt=default) = expr`
  (kernels tolerate extra keywords, so a whole data set can be splatted
  through [`relation_report`](@ref)),
- `variables(::Name) = (:x, :y, :z)` ‚Äî required variables only,
- `domain(::Name) = :domain`,
- registry insertion and `export Name`.

`residual`/`check` work immediately; `solve` works for every variable
the expression is affine in.  Note for downstream packages declaring
their own relations: the struct and methods precompile fine, but the
registry insertion is a load-time side effect ‚Äî re-register from your
module `__init__` if you need registry visibility across sessions.

```julia
@relation :scaling Rushbrooke(Œ±, Œ≤, Œ≥) = Œ± + 2Œ≤ + Œ≥ - 2
```
"""
macro relation(dom, defn)
    Meta.isexpr(defn, :(=), 2) ||
        error("@relation expects `Name(vars...) = expr`, got $defn")
    call, body = defn.args
    Meta.isexpr(call, :call) || error("@relation: left side must be a call, got $call")
    name = call.args[1]
    name isa Symbol || error("@relation: relation name must be a Symbol")
    required = Symbol[]
    kwargsig = Any[]
    for p in call.args[2:end]
        if p isa Symbol
            push!(required, p)
            push!(kwargsig, esc(p))
        elseif Meta.isexpr(p, :(=), 2) || Meta.isexpr(p, :kw, 2)
            push!(kwargsig, Expr(:kw, esc(p.args[1]), esc(p.args[2])))
        else
            error("@relation: cannot parse parameter $p")
        end
    end
    # kernels slurp extra keywords so relation_report can splat full data
    push!(kwargsig, Expr(:..., esc(gensym("extra"))))
    ename = esc(name)
    # NB: every generated method is module-QUALIFIED ‚Äî short-form
    # definitions inside a macro quote are otherwise hygienized into
    # gensym locals, and qualification also makes @relation usable from
    # downstream modules (methods land on OUR generics, on THEIR type ‚Äî
    # the intended extension pattern, not piracy).
    kernel_sig = Expr(
        :call, :(AbstractQAtlas._residual), Expr(:parameters, kwargsig...), :(::$ename)
    )
    kernel = Expr(:function, kernel_sig, esc(body))
    reqtuple = Expr(:tuple, (QuoteNode(s) for s in required)...)
    domval = QuoteNode(dom isa QuoteNode ? dom.value : dom)
    return quote
        Core.@__doc__ struct $ename <: AbstractQAtlas.AbstractRelation end
        $kernel
        AbstractQAtlas.variables(::$ename) = $reqtuple
        AbstractQAtlas.domain(::$ename) = $domval
        push!(AbstractQAtlas._RELATION_REGISTRY, $ename())
        $(Expr(:export, name))
        $ename
    end
end
export var"@relation"

# ‚îÄ‚îÄ‚îÄ Adoption surface: one-call verification over a data set ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

function _normalize_data(data::NamedTuple)
    return haskey(data, :T) && !haskey(data, :Œ≤) ? merge(data, (Œ≤=1 / data.T,)) : data
end

"""
    applicable_relations(data::NamedTuple; domain=nothing) -> Vector{AbstractRelation}

The registered relations whose required [`variables`](@ref) are all
present in `data` (after Œ≤-or-T normalization), optionally filtered by
[`domain`](@ref).  This is how a consumer package discovers what can be
checked against its observables ‚Äî no hand-listing.

Pass `domain` when mixing families in one data set: variable names are
matched literally, and physics overloads symbols across families (the
critical exponent `Œ≤` vs the inverse temperature `Œ≤`).
"""
function applicable_relations(data::NamedTuple; domain::Union{Nothing,Symbol}=nothing)
    d = _normalize_data(data)
    ks = keys(d)
    return filter(r -> all(v -> v in ks, variables(r)), all_relations(; domain=domain))
end
export applicable_relations

"""
    relation_report(data::NamedTuple; atol=0, domain=nothing)
        -> Vector{@NamedTuple{relation::AbstractRelation, residual::Number, pass::Bool}}

Evaluate every applicable relation against `data` and report per-relation
residuals.  The one-call integration point for downstream packages:

```julia
# gate an exponent table (an atlas registry, MC-extracted exponents, ‚Ä¶):
relation_report((; Œ±=0//1, Œ≤=1//8, Œ≥=7//4, Œ¥=15//1, ŒΩ=1//1, Œ∑=1//4, d=2))

# cross-check measured thermodynamics against every applicable identity:
relation_report((; C=c, var_E=v, Œ≤=Œ≤, N=N); atol=tol)
```
"""
function relation_report(data::NamedTuple; atol=0, domain::Union{Nothing,Symbol}=nothing)
    d = _normalize_data(data)
    out = @NamedTuple{relation::AbstractRelation, residual::Number, pass::Bool}[]
    for rel in applicable_relations(d; domain=domain)
        r = _residual(rel; d...)
        push!(out, (relation=rel, residual=r, pass=abs(r) <= atol))
    end
    return out
end
export relation_report

"""
    check_all(data::NamedTuple; atol=0, domain=nothing) -> Bool

`true` iff every applicable relation passes on `data` ‚Äî and at least one
relation applies: an empty match is `false`, never a silent green.
"""
function check_all(data::NamedTuple; atol=0, domain::Union{Nothing,Symbol}=nothing)
    report = relation_report(data; atol=atol, domain=domain)
    return !isempty(report) && all(row -> row.pass, report)
end
export check_all
